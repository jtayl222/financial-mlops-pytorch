import subprocess
import sys
import os
from pathlib import Path

def check_dependencies():
    """Check if required packages are installed"""
    required_packages = ['matplotlib', 'numpy', 'pandas']
    missing_packages = []
    
    for package in required_packages:
        try:
            __import__(package)
        except ImportError:
            missing_packages.append(package)
    
    if missing_packages:
        print(f"âŒ Missing required packages: {', '.join(missing_packages)}")
        print("Install them with: pip install matplotlib numpy pandas")
        return False
    
    return True

def ensure_output_directory():
    """Ensure output directory exists"""
    output_dir = Path('../images')
    output_dir.mkdir(exist_ok=True)
    return output_dir

def run_script(script_name):
    """Run a Python script and handle errors"""
    try:
        print(f"ğŸ”„ Generating {script_name}...")
        result = subprocess.run([sys.executable, script_name], 
                              capture_output=True, text=True, check=True)
        print(f"âœ… {script_name} completed successfully")
        return True
    except subprocess.CalledProcessError as e:
        print(f"âŒ Error running {script_name}:")
        print(f"   {e.stderr}")
        return False

def main():
    """Generate all publication images"""
    
    print("ğŸš€ MLOps Article Series Image Generator")
